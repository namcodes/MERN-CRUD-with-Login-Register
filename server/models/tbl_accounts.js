const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const tbl_users = new Schema({
  fname: {
    type: String,
    required: true,
  },
  lname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model("tbl_accounts", tbl_users);
